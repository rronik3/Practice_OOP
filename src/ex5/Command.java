package ex5;

/**
 * Інтерфейс Command визначає базовий контракт для виконання та скасування команд.
 * <p>
 * Використовується для реалізації шаблону Command, який дозволяє інкапсулювати
 * операції як об'єкти, забезпечуючи можливість їх виконання, скасування та повторного виконання.
 * </p>
 * 
 * @author xone
 * @version 1.0
 */
public interface Command {
    /**
     * Виконує команду.
     */
    void execute();

    /**
     * Скасовує виконання команди.
     */
    void undo();
}
